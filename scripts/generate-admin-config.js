#!/usr/bin/env node
/**
 * scripts/generate-admin-config.js
 *
 * Generates website/admin/config.js from environment variables.
 * Run this script as part of the Netlify build command (see netlify.toml).
 *
 * Required env vars:
 *   SUPABASE_URL       — Supabase project URL
 *   SUPABASE_ANON_KEY  — Supabase anon (public) key
 *
 * Optional:
 *   STORAGE_BUCKET     — defaults to 'portocoaststays-media'
 */

const fs   = require('fs');
const path = require('path');

const SUPABASE_URL  = process.env.SUPABASE_URL       || process.env.NEXT_PUBLIC_SUPABASE_URL;
const ANON_KEY      = process.env.SUPABASE_ANON_KEY  || process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;
const BUCKET        = process.env.STORAGE_BUCKET      || 'portocoaststays-media';

if (!SUPABASE_URL || !ANON_KEY) {
  console.error('[generate-admin-config] ERROR: SUPABASE_URL and SUPABASE_ANON_KEY must be set.');
  process.exit(1);
}

const config = `/* Auto-generated by scripts/generate-admin-config.js — DO NOT EDIT */
window.ADMIN_CONFIG = {
  SUPABASE_URL:      ${JSON.stringify(SUPABASE_URL)},
  SUPABASE_ANON_KEY: ${JSON.stringify(ANON_KEY)},
  STORAGE_BUCKET:    ${JSON.stringify(BUCKET)},
};
`;

const outPath = path.join(__dirname, '..', 'website', 'admin', 'config.js');
fs.writeFileSync(outPath, config, 'utf8');
console.log('[generate-admin-config] Written to', outPath);
